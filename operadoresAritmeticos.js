// OPERADORES DE ATRIBUIÇÃO

let salario = 500;
let bonus = 200;

// salario = salario + bonus
salario += bonus;
console.log(`Seu salario total é: ${salario}`);

let desconto = 100;
// salario = salario - desconto;
salario -= desconto;
console.log(`Seu salario final é: ${salario}`);

let multiplicacao = 2;
// salario = salario * multiplicacao;
salario *= multiplicacao
console.log(`Seu salário em dois meses será de: ${salario}`);

let divicao = 2;
// salario = salario / divicao;
salario /= divicao;
console.log(`: ${salario}`);